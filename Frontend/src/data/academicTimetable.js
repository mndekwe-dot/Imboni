// Helper: get all lessons a teacher has across all classes and days.
// Pass the live schedules state from DosTimetable to get real-time results.
export function getTeacherSchedule(teacherId, schedules = academicSchedules) {
    const result = []
    for (const [classId, days] of Object.entries(schedules)) {
        for (const [day, periods] of Object.entries(days)) {
            if (!periods) continue
            periods.forEach((period, index) => {
                if (period && 'teacherId' in period && period.teacherId === teacherId) {
                    result.push({ classId, day, periodIndex: index, ...period })
                }
            })
        }
    }
    return result
}
